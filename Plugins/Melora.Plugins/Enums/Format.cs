namespace Melora.Plugins.Enums;

/// <summary>
/// Describes the format of a track.
/// </summary>
public enum Format
{
    /// <summary>
    /// MPEG Audio Layer III.
    /// </summary>
    mp3,

    /// <summary>
    /// RIFF WAVE.
    /// </summary>
    wav,

    /// <summary>
    /// Advanced Audio Coding.
    /// </summary>
    m4a,

    /// <summary>
    /// Opus.
    /// </summary>
    ogg,

    /// <summary>
    /// Free Lossless Audio Codec.
    /// </summary>
    flac,

    /// <summary>
    /// Vorbis.
    /// </summary>
    webm
}